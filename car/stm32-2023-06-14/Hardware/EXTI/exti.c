#include "exti.h"
/**************************************************************************
外部中断初始化
**************************************************************************/
void EXTI_Init(void)
{
	RCC->APB2ENR|=1<<3;    //使能PORTB时钟	   	 
	GPIOB->CRH&=0XFFF0FFFF; 
	GPIOB->CRH|=0X00080000;//PB12上拉输入
  GPIOB->ODR|=1<<12;     //PB12上拉	
	Ex_NVIC_Config(GPIO_B,12,FTIR);		//下降沿触发
	MY_NVIC_Init(2,1,EXTI15_10_IRQn,2);  	//抢占2，子优先级1，组2
}













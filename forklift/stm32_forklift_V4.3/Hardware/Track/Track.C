#include "Track.h"
/**************************************************************************
循迹模块接口初始化
**************************************************************************/
void Track_Init(void)	
{	
	

	RCC->APB2ENR|=1<<2;    //使能PORTA时钟	   	 
	GPIOA->CRL&=0XFFFF0FFF; 
	GPIOA->CRL|=0X00004000;// PA3浮空输入	


	RCC->APB2ENR|=1<<3;    //使能PORTB时钟	   	 
	GPIOB->CRL&=0XFF000FFF; 
	GPIOB->CRL|=0X00444000;// PB4 5浮空输入
	
}
